//변수 - 메모리에 값 저장하기
package step02;

public class Exam03_3{
    public static void main(String []args){
    
        int a;
        a=100; //왼쪽 a라는 이름을 가진 메모리에 100이라는 값을 저장한다

        int b;
        b=a; //왼쪽 b 메모리에 a 메모리에 저장된 값을 저장한다

        System.out.println(a); //a라는 메모리의 값을 println()이 출력한다
        System.out.println(b);


    }
}
